const tempKelvinConstant = 459.67;
let celsius = ((fahrenheit) - 32) / (9 / 5); 
//calculate celsius and store in celsius variable
//(C) = ((F) - 32) / (9/5)
//print that value
//calculate kelvin value and store in variable
//T(K) = (T(°F) + 459.67)× 5/9
let kelvin = ((fahrenheit) + tempKelvinConstant) * (5 / 9) ;
//print that value
console.log(celsius);
